    const sample = document.createElement('div');
    const fontFamilyId = fontFamilyName.replace(/ /g, '_');
    sample.innerHTML = `<span style="white-space: no-wrap">${fontFamilyName}: </span><span id="${fontFamilyId}" style="font: 32px '${fontFamilyName}'; white-space: nowrap"></span>`;
    // sample.innerHTML = `<span>${fontFamilyName}</span><span style="font: 32px '${fontFamilyName}'; white-space: nowrap"> !&#x22;#$%&#x26;&#x27;()*+,-./0123456789:;&lt;=&gt;?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\]^_&#x60;abcdefghijklmnopqrstuvwxyz{|}~</span>`;
    document.getElementById("fontSamples").appendChild(sample);
    document.getElementById(fontFamilyId).innerText = document.getElementById("inputText").value;
