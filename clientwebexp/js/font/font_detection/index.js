function isFontAvailable(font, text) {
    const canvas = document.createElement('canvas');
    const context = canvas.getContext('2d');
    // const text = 'abcdefghijklmnopqrstuvwxyz0123456789';
    context.font = `40px '${font}', 'Adobe Blank'`;
    const newWidth = context.measureText(text).width;
    console.log(`${font}:${newWidth}`);
    return newWidth > 0;
}

function isFontSupportText(font, text) {
    const canvas = document.createElement('canvas');
    const context = canvas.getContext('2d');
    // const text = '';
    // context.font = `40px '${font}', 'Adobe Blank'`;
    // const newWidth = context.measureText(text).width;
    // if (newWidth == 0) {
    //     return false;
    // }

    canvas.width = 200;
    canvas.height = 200;

    context.font = `100px ''`;
    context.fillText(text, 10, 150);
    const imageDataDefault = context.getImageData(0, 0, 200, 200);

    context.clearRect(0, 0, 200, 200);
    context.font = `100px '${font}'`;
    context.fillText(text, 10, 150);

    const imageDataFont = context.getImageData(0, 0, 200, 200);
    if (imageDataDefault.data.length !== imageDataFont.data.length) {
        return false;
    }
    if (imageDataDefault.data.join('') === imageDataFont.data.join('')) {
        return false;
    }
    return true;
}

